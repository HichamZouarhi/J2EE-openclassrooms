package model;

public class Article {
	
	private String Article_ID;
	private String Name;
	
	public String getArticle_ID() {
		return Article_ID;
	}
	
	public void setArticle_ID(String article_ID) {
		Article_ID = article_ID;
	}
	
	public String getName() {
		return Name;
	}
	
	public void setName(String name) {
		Name = name;
	}
	
	
}
